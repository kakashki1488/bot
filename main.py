user_states = {}
last_messages = {}  # –î–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è ID –ø–æ—Å–ª–µ–¥–Ω–∏—Ö —Å–æ–æ–±—â–µ–Ω–∏–π

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–∏—Å–∫–∞ –≤–∏–¥–µ–æ –Ω–∞ YouTube
def search_youtube(topic):
    request = youtube.search().list(part="snippet", maxResults=1, q=topic, type='video', order='relevance')
    response = request.execute()
    return response['items'][0]['id']['videoId']

# –û—Ç–ø—Ä–∞–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è —Å –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏–µ–º
def send_message_with_tracking(chat_id, text, reply_markup=None,parse_mode=None):
    sent_message = bot.send_message(chat_id, text, reply_markup=reply_markup,parse_mode=parse_mode)
    last_messages[chat_id] = sent_message.message_id  # –°–æ—Ö—Ä–∞–Ω—è–µ–º ID –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è
    return sent_message

# –£–¥–∞–ª–µ–Ω–∏–µ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è
def delete_last_message(chat_id):
    if chat_id in last_messages:
        try:
            bot.delete_message(chat_id, last_messages[chat_id])
        except Exception as e:
            print(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —É–¥–∞–ª–µ–Ω–∏–∏ —Å–æ–æ–±—â–µ–Ω–∏—è: {e}")

# –ö–æ–º–∞–Ω–¥–∞ /start
@bot.message_handler(commands=['start'])
def send_welcome(message):
    chat_id = message.chat.id
    user_states[chat_id] = {"grade": None, "subject": None, "history": []}
    markup = types.InlineKeyboardMarkup()
    for grade in data.keys():
        markup.add(types.InlineKeyboardButton(grade, callback_data=f"grade_{grade}"))
    send_message_with_tracking(chat_id, "*–í—ã–±–µ—Ä–∏ –∫–ª–∞—Å—Å:*", reply_markup=markup,parse_mode="Markdown")

# –í—ã–±–æ—Ä –ø—Ä–µ–¥–º–µ—Ç–∞
@bot.callback_query_handler(func=lambda call: call.data.startswith("grade_"))
def select_subject(call):
    chat_id = call.message.chat.id
    grade = call.data.split("_")[1]
    user_states[chat_id]["grade"] = grade
    user_states[chat_id]["history"].append("grade")

    delete_last_message(chat_id)
    markup = types.InlineKeyboardMarkup()
