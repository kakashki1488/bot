@bot.callback_query_handler(func=lambda call: call.data == "back")
def go_back(call):
    chat_id = call.message.chat.id
    history = user_states.get(chat_id, {}).get("history", [])

    # –£–¥–∞–ª—è–µ–º —Ç–µ–∫—É—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ
    bot.delete_message(chat_id, call.message.message_id)

    if not history:
        bot.send_message(chat_id, "–í—ã —É–∂–µ –Ω–∞ –Ω–∞—á–∞–ª—å–Ω–æ–º —ç—Ç–∞–ø–µ. –ù–∞–∂–º–∏—Ç–µ /start, —á—Ç–æ–±—ã –Ω–∞—á–∞—Ç—å –∑–∞–Ω–æ–≤–æ.")
        return

    last_step = history.pop()  # –£–¥–∞–ª—è–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–π —à–∞–≥ –∏–∑ –∏—Å—Ç–æ—Ä–∏–∏

    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∫ –ø—Ä–µ–¥—ã–¥—É—â–µ–º—É —Å–æ—Å—Ç–æ—è–Ω–∏—é
    if last_step == "grade":
        send_welcome(call.message)
    elif last_step == "subject":
        grade = user_states[chat_id].get("grade")
        if grade:
            markup = types.InlineKeyboardMarkup()
            for subject in data[grade].keys():
                markup.add(types.InlineKeyboardButton(subject, callback_data=f"subject_{subject}"))
            markup.add(types.InlineKeyboardButton("‚¨ÖÔ∏è –ù–∞–∑–∞–¥", callback_data="back"))
